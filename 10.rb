puts "Enter the day"
day = gets.chomp

case day
when 'monday'
  puts "It's Monday!!"
when 'tuesday'
  puts "It's Tuesday!!"
when 'wednesday'
  puts "It's Wednesday!!"
when 'thursday'
  puts "It's Thursday!!"
when 'friday'
  puts "It's Friday!!"
when 'sunday'
  puts "It's Sunday!!"
when 'saturday'
  puts "It's Saturday!!"
else
  puts "Enter valid day"
end